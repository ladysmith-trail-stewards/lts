-- The auto_attach_block_deleted_at event trigger fires on CREATE TABLE and
-- would create the block_deleted_at trigger automatically. Disable it so we
-- can create the triggers manually with explicit args below.

alter event trigger auto_attach_block_deleted_at disable;

create table public.general_geom_collection (
  id          bigint generated by default as identity primary key,
  added_by    bigint references public.profiles (id) on delete set null,
  label       text not null,
  description text,
  style       jsonb not null default '{}'::jsonb,
  visibility  text not null default 'public'
               check (visibility in ('public', 'private', 'shared')),
  region_id   bigint not null references public.regions (id) on delete cascade,
  feature_collection_type text not null,
  created_at  timestamptz not null default now(),
  updated_at  timestamptz not null default now(),
  deleted_at  timestamptz default null
);

create table public.general_geom (
  id            bigint generated by default as identity primary key,
  collection_id bigint not null references public.general_geom_collection (id) on delete cascade,
  region_id     bigint not null references public.regions (id) on delete restrict,
  type          text not null,
  subtype       text,
  visibility    text not null default 'public'
                 check (visibility in ('public', 'private', 'shared')),
  label         text not null,
  description   text,
  geometry      geometry(Geometry, 4326) not null,
  created_at    timestamptz not null default now(),
  updated_at    timestamptz not null default now(),
  deleted_at    timestamptz default null
);

create trigger general_geom_collection_block_deleted_at
  before update on public.general_geom_collection
  for each row execute function public.block_deleted_at_update('REGION', 'REGION', 'NONE');

create trigger general_geom_block_deleted_at
  before update on public.general_geom
  for each row execute function public.block_deleted_at_update('REGION', 'REGION', 'NONE');

create index general_geom_collection_region_idx on public.general_geom_collection (region_id);
create index general_geom_collection_feature_collection_type_idx on public.general_geom_collection (feature_collection_type);
create index general_geom_collection_deleted_at_idx on public.general_geom_collection (deleted_at);
create index general_geom_collection_visibility_idx on public.general_geom_collection (visibility);
create index general_geom_collection_added_by_idx on public.general_geom_collection (added_by);

create index general_geom_collection_geom_idx on public.general_geom (collection_id);
create index general_geom_deleted_at_idx on public.general_geom (deleted_at);
create index general_geom_visibility_idx on public.general_geom (visibility);
create index general_geom_region_id_idx on public.general_geom (region_id);
create index general_geom_geometry_idx on public.general_geom using gist (geometry);

create trigger general_geom_collection_set_updated_at
  before update on public.general_geom_collection
  for each row execute function public.set_updated_at();

create trigger general_geom_set_updated_at
  before update on public.general_geom
  for each row execute function public.set_updated_at();

alter table public.general_geom_collection enable row level security;
alter table public.general_geom enable row level security;

