-- ============================================================
-- 12. Rewrite get_admin_users() RPC
-- ============================================================
drop function if exists public.get_admin_users();
create or replace function public.get_admin_users()
returns table (
  profile_id   bigint,
  auth_user_id uuid,
  email        text,
  name         text,
  role         public.app_role,
  region_name  text,
  phone        text,
  bio          text,
  created_at   timestamptz
)
language sql
security definer
stable
as $$
  select
    p.id          as profile_id,
    p.auth_user_id::uuid,
    u.email::text,
    p.name,
    p.role,
    r.name        as region_name,
    p.phone,
    p.bio,
    p.created_at
  from public.profiles p
  join auth.users u on u.id = p.auth_user_id::uuid
  join public.regions r on r.id = p.region_id
  where public.is_admin()
  order by p.created_at desc;
$$;

revoke execute on function public.get_admin_users() from public;
grant execute on function public.get_admin_users() to authenticated;

-- ============================================================
-- 13. Rewrite get_trails() RPC
-- ============================================================
drop function if exists public.get_trails(boolean);
create or replace function public.get_trails(
  hidden boolean default false
)
returns table (
  id             bigint,
  name           text,
  type           text,
  trail_class    text,
  activity_types text[],
  direction      text,
  hidden         boolean,
  planned        boolean,
  connector      boolean,
  bike           boolean,
  tf_popularity  numeric,
  visibility     text,
  region_id      bigint,
  geometry       json
)
language sql
security invoker
stable
as $$
  select
    t.id,
    t.name,
    t.type,
    t.trail_class,
    t.activity_types,
    t.direction,
    t.hidden,
    t.planned,
    t.connector,
    t.bike,
    t.tf_popularity,
    t.visibility,
    t.region_id,
    ST_AsGeoJSON(t.geometry)::json as geometry
  from public.trails t
  where
    (get_trails.hidden or not t.hidden)
  order by t.id;
$$;

grant execute on function public.get_trails(boolean) to anon, authenticated;
